#include <fdf.h>

void	fdf_exit(t_fdf *f)
{
	while (f->map_y--)
	{
		ft_memdel((void**)&(f->map[f->map_y]));
	}
	ft_memdel((void**)&f->map);
	mlx_destroy_window(f->mlx, f->win);
	exit(0);
}
